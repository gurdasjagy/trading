//! Rust-Native Exit Evaluator — Institutional Upgrade.
//!
//! Moves ALL exit logic (Parabolic SAR, ATR Trailing Stop, Chandelier Exit)
//! from the Python polling path into the Rust hot path. Exits are now evaluated
//! on EVERY tick from the live WebSocket stream, eliminating the 10-50ms Python
//! REST polling latency.
//!
//! # Integration
//!
//! Called from `tick_processor.rs` or `strategy_evaluator_loop` on every
//! BookSnapshot, immediately after the strategy evaluator runs. If an exit
//! is triggered, an OrderCommand with `order_type = MARKET` and `side = opposite`
//! is pushed directly into the execution SPSC ring.
//!
//! # Supported Exit Strategies
//!
//! 1. **Parabolic SAR** — Welles Wilder's classic trend-following stop.
//!    Accelerates toward price, triggers when SAR crosses price.
//!
//! 2. **ATR Trailing Stop** — Trails at N × ATR behind the extremum.
//!    Adapts to volatility automatically.
//!
//! 3. **Chandelier Exit** — Highest high (or lowest low) minus N × ATR.
//!    Designed for trending markets.
//!
//! 4. **Hard SL/TP** — Immediate trigger if price crosses the stop_loss
//!    or take_profit levels set on the OrderCommand.
//!
//! # Thread Safety
//!
//! `ExitEvaluator` is NOT thread-safe — it is owned by a single thread
//! (the strategy evaluator on Core 4). No locking needed.

