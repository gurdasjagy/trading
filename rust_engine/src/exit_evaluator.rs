// ATR Calculator
// ═══════════════════════════════════════════════════════════════════════════

/// Incremental ATR calculator using Wilder's smoothing (EMA with period N).
#[derive(Debug, Clone)]
struct AtrCalculator {
    /// Current ATR value.
    atr: f64,
    /// ATR period (number of bars).
    period: usize,
    /// Previous close for TR calculation.
    prev_close: f64,
    /// Number of samples received.
    sample_count: usize,
    /// Sum of initial TRs for warm-up.
    warmup_sum: f64,
}

impl AtrCalculator {
    fn new(period: usize) -> Self {
        Self {
            atr: 0.0,
            period,
            prev_close: 0.0,
            sample_count: 0,
            warmup_sum: 0.0,
        }
    }

    /// Update ATR with a new OHLC bar.
    /// Returns the current ATR value.
    fn update(&mut self, high: f64, low: f64, close: f64) -> f64 {
        if self.sample_count == 0 {
            self.prev_close = close;
            self.sample_count = 1;
            self.atr = high - low; // First bar: TR = high - low
            self.warmup_sum = self.atr;
            return self.atr;
        }

        // True Range = max(high - low, |high - prev_close|, |low - prev_close|)
        let tr = (high - low)
            .max((high - self.prev_close).abs())
            .max((low - self.prev_close).abs());

        self.sample_count += 1;

        if self.sample_count <= self.period {
            // Warm-up: simple average
            self.warmup_sum += tr;
            self.atr = self.warmup_sum / self.sample_count as f64;
        } else {
            // Wilder's smoothing: ATR = ((ATR * (N-1)) + TR) / N
            self.atr = ((self.atr * (self.period as f64 - 1.0)) + tr) / self.period as f64;
        }

        self.prev_close = close;
        self.atr
    }

    /// Get current ATR (returns 0 if not enough data).
    fn get(&self) -> f64 {
        self.atr
    }
}

