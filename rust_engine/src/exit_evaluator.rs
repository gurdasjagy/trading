// Parabolic SAR State
// ═══════════════════════════════════════════════════════════════════════════

/// Per-position Parabolic SAR state machine.
///
/// Implements Welles Wilder's Parabolic SAR with:
/// - Initial acceleration factor (AF) of 0.02
/// - AF increment of 0.02 per new extreme
/// - Maximum AF of 0.20
#[derive(Debug, Clone)]
struct ParabolicSarState {
    /// Current SAR value.
    sar: f64,
    /// Extreme point (highest high for longs, lowest low for shorts).
    extreme_point: f64,
    /// Current acceleration factor.
    af: f64,
    /// Whether this is tracking a long position.
    is_long: bool,
    /// Number of updates received.
    tick_count: u32,
}

impl ParabolicSarState {
    const AF_INITIAL: f64 = 0.02;
    const AF_INCREMENT: f64 = 0.02;
    const AF_MAX: f64 = 0.20;

    fn new(entry_price: f64, is_long: bool) -> Self {
        Self {
            sar: entry_price,
            extreme_point: entry_price,
            af: Self::AF_INITIAL,
            is_long,
            tick_count: 0,
        }
    }

    /// Update SAR with a new high/low tick.
    /// Returns `true` if the SAR was crossed (exit signal).
    fn update(&mut self, high: f64, low: f64) -> bool {
        self.tick_count += 1;

        // Need at least 2 ticks before generating signals
        if self.tick_count < 2 {
            if self.is_long {
                self.extreme_point = self.extreme_point.max(high);
            } else {
                self.extreme_point = self.extreme_point.min(low);
            }
            return false;
        }

        if self.is_long {
            // Long position: SAR is below price
            // Check if SAR was crossed (price fell below SAR)
            if low <= self.sar {
                return true; // Exit signal
            }

            // Update extreme point
            if high > self.extreme_point {
                self.extreme_point = high;
                self.af = (self.af + Self::AF_INCREMENT).min(Self::AF_MAX);
            }

            // Advance SAR
            self.sar = self.sar + self.af * (self.extreme_point - self.sar);

            // SAR must not be above the prior two lows (clamping)
            self.sar = self.sar.min(low);
        } else {
            // Short position: SAR is above price
            if high >= self.sar {
                return true; // Exit signal
            }

            if low < self.extreme_point {
                self.extreme_point = low;
                self.af = (self.af + Self::AF_INCREMENT).min(Self::AF_MAX);
            }

            self.sar = self.sar - self.af * (self.sar - self.extreme_point);
            self.sar = self.sar.max(high);
        }

        false
    }
}

