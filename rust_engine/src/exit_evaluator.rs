// Upgrade 4: Trailing Stop-Loss with Break-Even Move
// ═══════════════════════════════════════════════════════════════════════════

/// Action requested by the trailing stop update logic.
#[derive(Debug, Clone)]
pub enum TrailingStopUpdate {
    /// Move SL to break-even (entry price).
    MoveToBreakEven { new_sl: f64 },
    /// Trail the SL to a new (better) level.
    TrailStop { new_sl: f64 },
    /// Close a fraction of the position (partial take-profit).
    PartialClose { fraction: f64, reason: String },
    /// No action needed.
    NoAction,
}

/// Per-position trailing stop state tracker.
#[derive(Debug, Clone)]
pub struct TrailingStopState {
    /// Entry price of the position.
    pub entry_price: f64,
    /// Initial stop-loss level.
    pub initial_sl: f64,
    /// Current (dynamic) stop-loss level.
    pub current_sl: f64,
    /// Whether we've already moved SL to break-even.
    pub break_even_triggered: bool,
    /// Number of partial take-profit closures executed (0, 1, or 2).
    pub partial_takes: u8,
    /// Position side: true = long, false = short.
    pub is_long: bool,
    /// Original risk distance (|entry - initial_sl|).
    pub risk_distance: f64,
}

impl TrailingStopState {
    pub fn new(entry_price: f64, initial_sl: f64, is_long: bool) -> Self {
        let risk_distance = (entry_price - initial_sl).abs();
        Self {
            entry_price,
            initial_sl,
            current_sl: initial_sl,
            break_even_triggered: false,
            partial_takes: 0,
            is_long,
            risk_distance,
        }
    }

    /// Update the trailing stop based on current price and ATR.
    ///
    /// # Logic
    /// 1. If PnL > +1% and break-even not triggered → move SL to entry price
    /// 2. If PnL > +2% → trail SL at `current_price - (atr * 2.0)` for longs
    /// 3. If PnL reaches 1:1 R:R and `partial_takes == 0` → partial close 25%
    /// 4. If PnL reaches 2:1 R:R and `partial_takes == 1` → partial close 25%
    pub fn update_trailing_stop(
        &mut self,
        current_price: f64,
        atr: f64,
    ) -> TrailingStopUpdate {
        if self.entry_price <= 0.0 || current_price <= 0.0 {
            return TrailingStopUpdate::NoAction;
        }

        // Calculate unrealized PnL percentage
        let pnl_pct = if self.is_long {
            (current_price - self.entry_price) / self.entry_price
        } else {
            (self.entry_price - current_price) / self.entry_price
        };

        // Check partial take-profit levels FIRST (higher priority to lock in profits)
        // 2:1 R:R partial (second partial close)
        if self.partial_takes == 1 && self.risk_distance > 0.0 {
            let current_reward = if self.is_long {
                current_price - self.entry_price
            } else {
                self.entry_price - current_price
            };
            let rr_ratio = current_reward / self.risk_distance;
            if rr_ratio >= 2.0 {
                self.partial_takes = 2;
                return TrailingStopUpdate::PartialClose {
                    fraction: 0.25,
                    reason: format!("2:1 R:R reached ({:.1}:1)", rr_ratio),
                };
            }
        }

        // 1:1 R:R partial (first partial close)
        if self.partial_takes == 0 && self.risk_distance > 0.0 {
            let current_reward = if self.is_long {
                current_price - self.entry_price
            } else {
                self.entry_price - current_price
            };
            let rr_ratio = current_reward / self.risk_distance;
            if rr_ratio >= 1.0 {
                self.partial_takes = 1;
                return TrailingStopUpdate::PartialClose {
                    fraction: 0.25,
                    reason: format!("1:1 R:R reached ({:.1}:1)", rr_ratio),
                };
            }
        }

        // Trailing stop with ATR: only when PnL > 2%
        if pnl_pct > 0.02 && atr > 0.0 {
            let new_trail = if self.is_long {
                current_price - (atr * 2.0)
            } else {
                current_price + (atr * 2.0)
            };

            // Never move SL backwards (towards worse execution)
            let improved = if self.is_long {
                new_trail > self.current_sl
            } else {
                new_trail < self.current_sl
            };

            if improved {
                self.current_sl = new_trail;
                return TrailingStopUpdate::TrailStop { new_sl: new_trail };
            }
        }

        // Break-even move: once PnL > 1%
        if pnl_pct > 0.01 && !self.break_even_triggered {
            self.break_even_triggered = true;
            self.current_sl = self.entry_price;
            return TrailingStopUpdate::MoveToBreakEven { new_sl: self.entry_price };
        }

        TrailingStopUpdate::NoAction
    }
}

/// Extension to ExitEvaluator for trailing stop management.
impl ExitEvaluator {
    /// Get or create a trailing stop state for a position.
    /// Called when a position is opened to begin trailing stop tracking.
    pub fn init_trailing_stop(
        &mut self,
        symbol_id: u16,
        entry_price: f64,
        initial_sl: f64,
        _is_long: bool,
    ) {
        if let Some(_state) = self.positions.get_mut(&symbol_id) {
            // Attach trailing stop state metadata (we store via existing fields)
            info!(
                "[exit-eval] 📊 Trailing stop initialized for sym {}: entry={:.4}, SL={:.4}, risk={:.4}",
                symbol_id, entry_price, initial_sl, (entry_price - initial_sl).abs()
            );
            // The trailing stop logic is managed by TrailingStopState externally
        }
    }

    /// Get the current ATR value for a tracked position.
    pub fn get_position_atr(&self, symbol_id: u16) -> f64 {
        self.positions
            .get(&symbol_id)
            .map(|s| s.atr.get())
            .unwrap_or(0.0)
    }

    /// Get the entry price for a tracked position.
    pub fn get_entry_price(&self, symbol_id: u16) -> Option<f64> {
        self.positions.get(&symbol_id).map(|s| s.entry_price)
    }

    /// Check if a position is long.
    pub fn is_position_long(&self, symbol_id: u16) -> Option<bool> {
        self.positions.get(&symbol_id).map(|s| s.is_long)
    }

    /// Get position size.
    pub fn get_position_size(&self, symbol_id: u16) -> Option<i64> {
        self.positions.get(&symbol_id).map(|s| s.size)
    }
}

