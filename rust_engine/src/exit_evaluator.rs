// ExitEvaluator — manages all position exits
// ═══════════════════════════════════════════════════════════════════════════

/// Manages exit evaluation for all active positions.
///
/// Owned by the strategy evaluator thread. NOT thread-safe.
pub struct ExitEvaluator {
    /// Active position exit states, keyed by a composite key (symbol_id).
    positions: HashMap<u16, PositionExitState>,
    /// Total exits triggered.
    pub total_exits: u64,
    /// Exits by reason.
    pub exits_by_reason: HashMap<String, u64>,
}

impl ExitEvaluator {
    pub fn new() -> Self {
        Self {
            positions: HashMap::with_capacity(16),
            total_exits: 0,
            exits_by_reason: HashMap::new(),
        }
    }

    /// Register a new position for exit tracking.
    pub fn track_position(
        &mut self,
        symbol_id: u16,
        is_long: bool,
        entry_price: f64,
        stop_loss: f64,
        take_profit: f64,
        size: i64,
        ttl_ns: u64,
    ) {
        let state = PositionExitState::new(
            symbol_id, is_long, entry_price, stop_loss, take_profit, size, ttl_ns,
        );
        self.positions.insert(symbol_id, state);
        info!(
            "[exit-eval] Tracking {} position for sym {} @ {:.4} SL={:.4} TP={:.4}",
            if is_long { "LONG" } else { "SHORT" },
            symbol_id, entry_price, stop_loss, take_profit
        );
    }

    /// Remove a position from exit tracking (after exit order is submitted).
    pub fn untrack_position(&mut self, symbol_id: u16) {
        self.positions.remove(&symbol_id);
    }

    /// Evaluate all tracked positions against the current market data.
    /// Returns a list of exit signals for positions that should be closed.
    ///
    /// `high`, `low`, `close` are synthetic from the book snapshot:
    /// - high = best_ask (worst case for longs)
    /// - low  = best_bid (worst case for shorts)
    /// - close = mid_price
    pub fn evaluate_all(
        &mut self,
        symbol_id: u16,
        high: f64,
        low: f64,
        close: f64,
        mid_price: f64,
    ) -> Vec<ExitSignal> {
        let mut signals = Vec::new();

        if let Some(state) = self.positions.get_mut(&symbol_id) {
            if let Some(signal) = state.evaluate(high, low, close, mid_price) {
                info!(
                    "[exit-eval] 🚨 EXIT triggered for sym {}: reason={}, urgency={}",
                    symbol_id, signal.reason, signal.urgency
                );
                self.total_exits += 1;
                *self.exits_by_reason
                    .entry(signal.reason.to_string())
                    .or_insert(0) += 1;
                signals.push(signal);
            }
        }

        signals
    }

    /// Check if a position is being tracked for a symbol.
    pub fn is_tracking(&self, symbol_id: u16) -> bool {
        self.positions.contains_key(&symbol_id)
    }

    /// CHECK #4: Block duplicate positions per symbol.
    /// Returns true if the symbol already has an open position being tracked.
    pub fn has_position(&self, symbol_id: u16) -> bool {
        self.positions.contains_key(&symbol_id)
    }

    /// Get the number of positions being tracked.
    pub fn tracked_count(&self) -> usize {
        self.positions.len()
    }

    /// Update the SL/TP for a tracked position (dynamic adjustment).
    pub fn update_sl_tp(&mut self, symbol_id: u16, new_sl: f64, new_tp: f64) {
        if let Some(state) = self.positions.get_mut(&symbol_id) {
            if new_sl > 0.0 {
                state.stop_loss = new_sl;
            }
            if new_tp > 0.0 {
                state.take_profit = new_tp;
            }
        }
    }

    /// Detect unprotected positions (no SL and/or no TP set).
    ///
    /// Returns a list of (symbol_id, is_long, entry_price, has_sl, has_tp) for
    /// positions that are missing SL or TP protection. The caller is responsible
    /// for computing and applying appropriate SL/TP values (e.g. ATR-based).
    pub fn get_unprotected_positions(&self) -> Vec<(u16, bool, f64, bool, bool)> {
        let mut unprotected = Vec::new();
        for (_, state) in &self.positions {
            let has_sl = state.stop_loss > 0.0;
            let has_tp = state.take_profit > 0.0;
            if !has_sl || !has_tp {
                unprotected.push((
                    state.symbol_id,
                    state.is_long,
                    state.entry_price,
                    has_sl,
                    has_tp,
                ));
            }
        }
        unprotected
    }
}

impl Default for ExitEvaluator {
    fn default() -> Self {
        Self::new()
    }
}

