/// Complete exit tracking state for a single position.
#[derive(Debug, Clone)]
struct PositionExitState {
    /// Symbol ID.
    symbol_id: u16,
    /// Position side: true = long, false = short.
    is_long: bool,
    /// Entry price.
    entry_price: f64,
    /// Hard stop loss price (0 = none).
    stop_loss: f64,
    /// Hard take profit price (0 = none).
    take_profit: f64,
    /// Position size in contracts.
    size: i64,
    /// Timestamp when position was opened (nanoseconds).
    opened_ns: u64,
    /// Maximum TTL in nanoseconds (0 = no expiry).
    ttl_ns: u64,

    /// Parabolic SAR state.
    psar: ParabolicSarState,
    /// ATR calculator (14-period).
    atr: AtrCalculator,
    /// ATR trailing stop multiplier.
    atr_multiplier: f64,
    /// Highest high since entry (for longs) / lowest low (for shorts).
    extremum: f64,
    /// Current trailing stop level.
    trailing_stop: f64,
    /// Chandelier exit multiplier.
    chandelier_multiplier: f64,
}

impl PositionExitState {
    fn new(
        symbol_id: u16,
        is_long: bool,
        entry_price: f64,
        stop_loss: f64,
        take_profit: f64,
        size: i64,
        ttl_ns: u64,
    ) -> Self {
        Self {
            symbol_id,
            is_long,
            entry_price,
            stop_loss,
            take_profit,
            size,
            opened_ns: now_ns(),
            ttl_ns,
            psar: ParabolicSarState::new(entry_price, is_long),
            atr: AtrCalculator::new(14),
            atr_multiplier: 3.0,
            extremum: entry_price,
            trailing_stop: if is_long {
                entry_price * 0.97 // Initial 3% trailing for longs
            } else {
                entry_price * 1.03 // Initial 3% trailing for shorts
            },
            chandelier_multiplier: 3.0,
        }
    }

    /// Evaluate all exit conditions against the current tick.
    /// Returns `Some(ExitSignal)` if any exit condition is met.
    fn evaluate(
        &mut self,
        high: f64,
        low: f64,
        close: f64,
        mid_price: f64,
    ) -> Option<ExitSignal> {
        let exit_side = if self.is_long { 1u8 } else { 0u8 }; // opposite side

        // 1. Hard Stop Loss check (highest priority)
        if self.stop_loss > 0.0 {
            if self.is_long && mid_price <= self.stop_loss {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::HardStopLoss,
                    urgency: 2, // emergency
                    suggested_price_fp: 0, // market order
                });
            }
            if !self.is_long && mid_price >= self.stop_loss {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::HardStopLoss,
                    urgency: 2,
                    suggested_price_fp: 0,
                });
            }
        }

        // 2. Take Profit check
        if self.take_profit > 0.0 {
            if self.is_long && mid_price >= self.take_profit {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::TakeProfit,
                    urgency: 1,
                    suggested_price_fp: 0,
                });
            }
            if !self.is_long && mid_price <= self.take_profit {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::TakeProfit,
                    urgency: 1,
                    suggested_price_fp: 0,
                });
            }
        }

        // 3. Time-based expiry
        if self.ttl_ns > 0 {
            let elapsed = now_ns().saturating_sub(self.opened_ns);
            if elapsed > self.ttl_ns {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::TimeExpiry,
                    urgency: 1,
                    suggested_price_fp: 0,
                });
            }
        }

        // 4. Update ATR
        let atr = self.atr.update(high, low, close);

        // 5. Update extremum and ATR trailing stop
        if self.is_long {
            if high > self.extremum {
                self.extremum = high;
            }
            if atr > 0.0 {
                let new_trail = self.extremum - self.atr_multiplier * atr;
                // Trailing stop only moves up for longs
                self.trailing_stop = self.trailing_stop.max(new_trail);
            }
            if mid_price <= self.trailing_stop {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::AtrTrailingStop,
                    urgency: 1,
                    suggested_price_fp: 0,
                });
            }
        } else {
            if low < self.extremum {
                self.extremum = low;
            }
            if atr > 0.0 {
                let new_trail = self.extremum + self.atr_multiplier * atr;
                // Trailing stop only moves down for shorts
                self.trailing_stop = self.trailing_stop.min(new_trail);
            }
            if mid_price >= self.trailing_stop {
                return Some(ExitSignal {
                    symbol_id: self.symbol_id,
                    exit_side,
                    reason: ExitReason::AtrTrailingStop,
                    urgency: 1,
                    suggested_price_fp: 0,
                });
            }
        }

        // 6. Chandelier Exit
        if atr > 0.0 {
            if self.is_long {
                let chandelier = self.extremum - self.chandelier_multiplier * atr;
                if mid_price <= chandelier {
                    return Some(ExitSignal {
                        symbol_id: self.symbol_id,
                        exit_side,
                        reason: ExitReason::ChandelierExit,
                        urgency: 1,
                        suggested_price_fp: 0,
                    });
                }
            } else {
                let chandelier = self.extremum + self.chandelier_multiplier * atr;
                if mid_price >= chandelier {
                    return Some(ExitSignal {
                        symbol_id: self.symbol_id,
                        exit_side,
                        reason: ExitReason::ChandelierExit,
                        urgency: 1,
                        suggested_price_fp: 0,
                    });
                }
            }
        }

        // 7. Parabolic SAR
        if self.psar.update(high, low) {
            return Some(ExitSignal {
                symbol_id: self.symbol_id,
                exit_side,
                reason: ExitReason::ParabolicSarCross,
                urgency: 1,
                suggested_price_fp: 0,
            });
        }

        None
    }
}

