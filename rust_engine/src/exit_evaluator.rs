/// An exit signal generated by the evaluator.
#[derive(Debug, Clone, Copy)]
pub struct ExitSignal {
    /// Symbol ID of the position to close.
    pub symbol_id: u16,
    /// Side of the EXIT order (opposite of position). 0 = buy, 1 = sell.
    pub exit_side: u8,
    /// Reason for exit.
    pub reason: ExitReason,
    /// Urgency: 0 = normal, 1 = high, 2 = emergency.
    pub urgency: u8,
    /// Suggested exit price (0 = market order).
    pub suggested_price_fp: i64,
}

/// Why an exit was triggered.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ExitReason {
    /// Parabolic SAR crossed price.
    ParabolicSarCross,
    /// ATR trailing stop hit.
    AtrTrailingStop,
    /// Chandelier exit triggered.
    ChandelierExit,
    /// Hard stop loss price breached.
    HardStopLoss,
    /// Take profit price reached.
    TakeProfit,
    /// Time-based expiry (TTL exceeded).
    TimeExpiry,
    /// Upgrade 4: Trailing stop moved to break-even.
    TrailingBreakEven,
    /// Upgrade 4: Trailing stop (ATR-based) was hit.
    TrailingAtrStop,
    /// Upgrade 4: Partial take-profit at R:R level.
    PartialTakeProfit,
}

impl std::fmt::Display for ExitReason {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            ExitReason::ParabolicSarCross => write!(f, "psar_cross"),
            ExitReason::AtrTrailingStop => write!(f, "atr_trail"),
            ExitReason::ChandelierExit => write!(f, "chandelier"),
            ExitReason::HardStopLoss => write!(f, "hard_sl"),
            ExitReason::TakeProfit => write!(f, "hard_tp"),
            ExitReason::TimeExpiry => write!(f, "time_expiry"),
            ExitReason::TrailingBreakEven => write!(f, "trailing_be"),
            ExitReason::TrailingAtrStop => write!(f, "trailing_atr"),
            ExitReason::PartialTakeProfit => write!(f, "partial_tp"),
        }
    }
}

