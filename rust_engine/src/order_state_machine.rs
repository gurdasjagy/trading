#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_happy_path() {
        let mut sm = OrderStateMachine::new();
        sm.track_order("test-1".into(), "BTC_USDT".into());

        let s = sm
            .apply_event(
                "test-1",
                OrderEvent::Submitted {
                    exchange_oid: Some("ex-123".into()),
                },
            )
            .unwrap();
        assert_eq!(s, OrderState::Submitted);

        let s = sm
            .apply_event(
                "test-1",
                OrderEvent::Acknowledged {
                    exchange_oid: "ex-123".into(),
                },
            )
            .unwrap();
        assert_eq!(s, OrderState::Acknowledged);

        let s = sm
            .apply_event(
                "test-1",
                OrderEvent::FullFill {
                    avg_price: 50000.0,
                    total_qty: 10,
                },
            )
            .unwrap();
        assert_eq!(s, OrderState::Filled);
    }

    #[test]
    fn test_rejection() {
        let mut sm = OrderStateMachine::new();
        sm.track_order("test-2".into(), "ETH_USDT".into());

        let s = sm
            .apply_event(
                "test-2",
                OrderEvent::Rejected {
                    reason: "insufficient margin".into(),
                },
            )
            .unwrap();
        assert_eq!(s, OrderState::Rejected);
        assert!(sm.orders["test-2"].state.is_terminal());
    }

    #[test]
    fn test_timeout_detection() {
        let mut sm = OrderStateMachine::new();
        sm.order_timeout = Duration::from_millis(1); // Instant timeout for test
        sm.track_order("test-3".into(), "SOL_USDT".into());

        let _ = sm.apply_event(
            "test-3",
            OrderEvent::Submitted {
                exchange_oid: Some("ex-456".into()),
            },
        );

        std::thread::sleep(Duration::from_millis(5));
        let timeouts = sm.check_timeouts();
        assert_eq!(timeouts.len(), 1);
        assert_eq!(timeouts[0].0, "test-3");
    }

    #[test]
    fn test_reconciliation_force_cancel() {
        let mut sm = OrderStateMachine::new();
        sm.track_order("test-4".into(), "BNB_USDT".into());

        let _ = sm.apply_event(
            "test-4",
            OrderEvent::Submitted {
                exchange_oid: Some("ex-789".into()),
            },
        );

        // Simulate reconciliation finding order cancelled on exchange
        let s = sm
            .apply_event(
                "test-4",
                OrderEvent::Reconciled {
                    exchange_state: "cancelled".into(),
                },
            )
            .unwrap();
        assert_eq!(s, OrderState::Cancelled);
    }
}
